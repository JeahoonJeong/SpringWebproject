package com.spring.webproject.dto;

public class AdminWarehouseDTO {
	private String warehouseId;
	private String warehouseName;
	private String lat;
	private String lng;
	private String addr;
	private String time;
	private String intro;
	private String waybus;
	private String waysub;
	
	
	public String getWarehouseId() {
		return warehouseId;
	}
	public void setWarehouseId(String warehouseId) {
		this.warehouseId = warehouseId;
	}
	public String getWarehouseName() {
		return warehouseName;
	}
	public void setWarehouseName(String warehouseName) {
		this.warehouseName = warehouseName;
	}
	public String getLat() {
		return lat;
	}
	public void setLat(String lat) {
		this.lat = lat;
	}
	public String getLng() {
		return lng;
	}
	public void setLng(String lng) {
		this.lng = lng;
	}
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getIntro() {
		return intro;
	}
	public void setIntro(String intro) {
		this.intro = intro;
	}
	public String getWaybus() {
		return waybus;
	}
	public void setWaybus(String waybus) {
		this.waybus = waybus;
	}
	public String getWaysub() {
		return waysub;
	}
	public void setWaysub(String waysub) {
		this.waysub = waysub;
	}
	
	
}
